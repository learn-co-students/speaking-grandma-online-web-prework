    "I LOVE YOU TOO PUMPKIN!" 
  elsif require_relative == "Hi Nana, how are you?"
     "HUH?! SPEAK UP, SONNY!".upcase
  elsif require_relative == "Hi!"
     "HUH?! SPEAK UP, SONNY!"
  elsif require_relative == "WHAT DID YOU EAT TODAY?"
     "NO, NOT SINCE 1938!"
  else require_relative == "WHAT?"
     "NO, NOT SINCE 1938!"
end
end